package com.mon.projet;

// Couple.java

public class Couple 
{
    private int first;
    private int second;

    public Couple(int first, int second) 
    {
        this.first = first;
        this.second = second;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }
}
